package leetcode203;

public class Solution {
    public ListNode removeElements(ListNode head, int val) {
        ListNode hair = new ListNode(0, head);
        ListNode prev = hair;
        while (head != null) {
            if (head.val == val) {
                prev.next = head.next;
            } else {
                prev = head;
            }
            head = head.next;
        }
        return hair.next;
    }
}

class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}